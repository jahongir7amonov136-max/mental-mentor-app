const I18nContext = createContext<{ lang: Lang; t: (k: string) => string; setLang: (l: Lang) => void }>(
  { lang: "uz", t: (k) => k, setLang: () => {} }
);

const LANG_KEY = "swsp_lang";

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>("uz");
  useEffect(() => {
    AsyncStorage.getItem(LANG_KEY).then((v) => {
      if (v === "uz" || v === "ru" || v === "en") setLangState(v);
    });
  }, []);
  const setLang = useCallback((l: Lang) => { setLangState(l); AsyncStorage.setItem(LANG_KEY, l); }, []);
  const t = useCallback((k: string) => STRINGS[lang][k] ?? k, [lang]);
  return <I18nContext.Provider value={{ lang, t, setLang }}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}
